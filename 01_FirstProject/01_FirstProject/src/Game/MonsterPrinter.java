    public static void printMonsterBattleScreen(String monsterName) {
        for (String key : monsterArtMap.keySet()) {
            if (monsterName.matches(key + ".*")) {
                System.out.flush();
                for (String line : monsterArtMap.get(key)) {
                    System.out.println(line);
                }
                return;
            }
        }
        System.out.println("⚠️ 알 수 없는 몬스터입니다.");
    }
